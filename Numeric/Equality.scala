object Equ {
    def main(args: Array[String]): Unit = {
        val a = 5
        val b = 3

        // Equal to (==) operator
        if (a == b) {
            println("a is equal to b")
        } else {
            println("a is not equal to b")
        }

        // Not equal to (!=) operator
        if (a != b) {
            println("a is not equal to b")
        } else {
            println("a is equal to b")
        }
        val a1 = 5
        val b1= 3

        // Greater than or equal to (>=) operator
        if (a1 >= b1) {
            println("a is greater than or equal to b")
        } else {
            println("a is less than b")
        }

    }
}